"""من الشروط : كل سنة تقبل القسمة على 4 = كبيسة
 ولكن بكل 100 عام نتخطى سنة
  إلا إذا كانت تقبل القسمة على 400 فهي كبيسة"""


year = int(input(" أكتب السنة :"))
if (year % 4 == 0 and year % 100 != 0):
    print(f"لانها تقبل القسمة على 4 ولاتقبل القسمة على 100\n{year}  سنة كبيسة")
elif (year % 400 == 0):
    print(f"لانها تقبل القسمة على 400\n{year}  سنة كبيسة")
else:
    print(f"  {year}  سنة ليست كبيسة")